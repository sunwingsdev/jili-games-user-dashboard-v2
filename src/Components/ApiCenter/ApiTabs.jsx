const ApiTabs = () => {
  const ref = useRef(null);
  const [active, setActive] = useState(tabs[0].key);
  const [canScroll, setCanScroll] = useState({ left: false, right: true });

  useEffect(() => {
    const el = ref.current;
    const check = () => {
      setCanScroll({
        left: el.scrollLeft > 0,
        right: el.scrollLeft + el.clientWidth < el.scrollWidth,
      });
    };
    if (el) {
      el.addEventListener("scroll", check);
      check();
      return () => el.removeEventListener("scroll", check);
    }
  }, []);

  const scroll = (dir) => {
    ref.current?.scrollBy({
      left: ref.current.clientWidth * 0.7 * (dir === "left" ? -1 : 1),
      behavior: "smooth",
    });
  };

     const { Component } = tabs.find((t) => t.key === active);

  return (
    <div className="flex flex-col items-center w-full">
      {/* Make this div relative so arrows can be absolutely positioned inside it */}
      <div className="relative w-full">
        {/* Left Arrow */}
        <div className="absolute -left-5 top-1/2 -translate-y-1/2 z-10">
          <button
            onClick={() => scroll("left")}
            disabled={!canScroll.left}
            className="text-textYellow"
          >
            <FaChevronLeft size={20} />
          </button>
        </div>

        {/* Scrollable Tab List (make this the bg-black container) */}
        <div
          ref={ref}
          className="flex space-x-4 overflow-x-auto scrollbar-hide w-full scroll-smooth bg-black relative"
        >
          {tabs.map(({ key, label, path }) => (
            <button
              key={key}
              onClick={() => setActive(key)}
              className={`flex flex-col items-center px-4 py-2 min-w-[100px] whitespace-nowrap border-b-2 ${
                active === key
                  ? "border-textSecondary text-textOrange"
                  : "border-transparent text-textYellow"
              }`}
            >
              <svg
                viewBox="0 0 80 80"
                fill="currentColor"
                className="w-10 h-10 mb-1"
              >
                {Array.isArray(path) ? (
                  path.map((d, i) => <path key={i} d={d} />)
                ) : (
                  <path d={path} />
                )}
              </svg>
              <span className="text-xs">{label}</span>
            </button>
          ))}
        </div>

        {/* Right Arrow */}
        <div className="absolute -right-5 top-1/2 -translate-y-1/2 z-10">
          <button
            onClick={() => scroll("right")}
            disabled={!canScroll.right}
            className="text-textYellow"
          >
            <FaChevronRight size={20} />
          </button>
        </div>
      </div>

      {/* Tab Content Below */}
      <div className="w-full mt-6">
    <Component />
  </div>
    </div>
  );
};
export default ApiTabs;
